int     ft_atoi(const char *str)
{
    int nbr;
    int check;

    nbr = 0;
    check = 1;
    while ((*str == '\t') || (*str == '\n') || (*str == '\v') || (*str == '\f') || (*str == '\r') || (*str == ' '))
        str++;
    if (*str == 0)
        return (0);
    if (*str == '-' || *str == '+')
        if (*str == '-')
            check = -1;
        str++;
    while (*str && (*str >= 0 && *str <= 9))
    {
        nbr = (nbr * 10) + (*str - 48) * check;
        str++;
    }
    return (nbr);
}