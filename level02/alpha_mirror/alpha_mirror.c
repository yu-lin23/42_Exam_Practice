#include <unistd.h>

int main(int ac, char **av)
{
    int i;
    char j;

    if (ac != 2)
        write(1, "\n", 1);
    else
    {
        i = 0;
        while (av[1][i] != '\0')
            {
                if (av[1][i] >= 'A' && av[1][i] <= 'Z')
                    j = 77 - (av[1][i] - 78);
                else if (av[1][i] >= 'a' && av[1][i] <= 'z')
                    j = 109 - (av[1][i] - 110);
                else
                    j = av[1][i];
                write(1, &j, 1);
                i++;

            }
        write(1, "\n", 1);
        return (0);
    }
}